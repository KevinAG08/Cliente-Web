let calcScrollValue = () =>{
    let scrollProgress = document.getElementById("progress");
    let progress50 = document.getElementById("progress-50");
    let progress51 = document.getElementById("progress-51");
    let progress52 = document.getElementById("progress-52");
    let progress53 = document.getElementById("progress-53");
    let progress54 = document.getElementById("progress-54");
    let progress55 = document.getElementById("progress-55");
    let progressValue = document.getElementById("progress-value");
    let flecha = document.getElementById("flecha");
    let pos = document.documentElement.scrollTop;
    let calcHeight = document.documentElement.scrollHeight - document.documentElement.clientHeight;
    let scrollValue = Math.round((pos * 100) / calcHeight);
    
    console.log(scrollValue);
    progress50.style.width = scrollValue + '%';
    progress51.style.width = scrollValue + '%';
    progress52.style.width = scrollValue + '%';
    progress53.style.width = scrollValue + '%';
    progress54.style.width = scrollValue + '%';
    progress55.style.width = scrollValue + '%';
    if(pos>100){
        scrollProgress.style.display = "grid";
    } else{
        scrollProgress.style.display = "none";
    }

    if(scrollValue==100 || screen.width>=600){
        flecha.style.display = "none";
    } else{
        flecha.style.display = "inline";
    }
    
    scrollProgress.addEventListener("click", () => {
        document.documentElement.scrollTop = 0;
    })

    scrollProgress.style.background = `conic-gradient(#0202c6 ${scrollValue}%, #d7d7d7 ${scrollValue}%)`;
};

window.onscroll = calcScrollValue;
window.onload = calcScrollValue;


